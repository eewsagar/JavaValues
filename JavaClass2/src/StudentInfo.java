//pojo -  plain old java object 
public class StudentInfo {
	private String stuName;
	private int roll_no;
	// getter
	public String getStuName() {
		return stuName;
	}
    // setter
	public void setStuName(String name) {
		this.stuName =  name;
		
	}
	
	public int getRoll_no() {
		return roll_no;
	}

	public void setRoll_no(int roll) {
		this.roll_no =  roll;
		
	}
	
}
